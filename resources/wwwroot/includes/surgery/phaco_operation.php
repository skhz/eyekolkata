                    <div>
                    	Phacoemulsification is a surgical method used to remove a cataract, which is a clouding of the eye's naturally clear lens. A cloudy lens interferes with light passing through to the retina, the light-sensing layer of cells at the back of the eye. Having a cataract can be compared to looking at the world through a foggy window. In phacoemulsification, an ultrasonic oscillating probe is inserted into the eye. The probe breaks up the center of the lens. The fragments are suctioned from the eye at the same time. A small incision that often does not require sutures to close can be used since the cataract is removed in tiny pieces. Most of the lens capsule is left behind and a foldable intraocular lens implant, or IOL, is placed permanently inside to help focus light onto the retina. Vision returns quickly and one can resume normal activities within a short period of time.
